	Harremana		VARCHAR2(20),
	CONSTRAINT PK_EUKI_Izena FOREIGN KEY (IzenaMez) REFERENCES MEZENAS ON DELETE CASCADE,
	CONSTRAINT PK_EUKI_Pintorea FOREIGN KEY (KodPintorea) REFERENCES PINTOREA ON DELETE CASCADE
