}
CREATE TABLE MEZENAS
{
	IzenaMez		VARCHAR2(30),
	Jaioterria		VARCHAR2(20),
	Nazionalitatea	VARCHAR2(20),
	JaiotzaDataMez	DATE,
	HeriotzaDataMez	DATE,
	PRIMARY KEY IzenaMez
}
CREATE TABLE EUKI
{
	KodPintorea		NUMBER(10),
